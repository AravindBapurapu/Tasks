# Creating an int variable, float variable, string variable and assign the
# values to the above three variable names.

Integer = 10        #by default python takes it as an integer value
Float = 50.2        #by default python takes it as a float value
String  = "Python"  #by default python takes it as a string--> because of quotes(single or double)

print("---Data types---")
print()
print("Integer is:",Integer)
print("Type of Integer is: ",type(Integer))
print()
print("float is:", Float)
print("type of float is:", type(Float))
print()
print("String value is:",String)
print("type of string is:", type(String))

