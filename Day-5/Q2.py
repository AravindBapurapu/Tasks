# Numbers consist of all the numeric values. Numbers in Python are
# classified into the following data types.
# ❖ int - Integers don’t have decimal and can have any length as long as the
# required memory is available. For example, 3, 855, etc.
# ❖ float - Floating point numbers are the ones having decimal. For example,
# 2.564728.
# ❖ complex - Complex numbers, as in Mathematics, have the form a + bj,
# where a is the real part and b is the imaginary part. For example, 2 + 3j, 6j.
'''integer example'''
x = 10
y = 10.1
z = 2 - 5j

print("===types of Numbers or Numeric values===")
print()
print(f"{x} and its data type is: {type(x)}")  # integer have no decimals
print()
print(f"{y} and its data type is: {type(y)}")   #floating value has decimals
print()
print(f"{z} and its data type is: {type(z)}")   #complex value have imaginary part -> here 2 is real and 5j is imaginary part